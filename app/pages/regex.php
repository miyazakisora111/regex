        function escapeHtml(str) {
            return str
                .replace(/&/g, '&amp;')
                .replace(/</g, '&lt;')
                .replace(/>/g, '&gt;');
        }

        function render() {
            let regex;
            try {
                regex = new RegExp(patternInput.value, 'g');
            } catch {
                preview.textContent = textArea.value;
                return;
            }

            const raw = textArea.value;
            const safe = escapeHtml(raw);

            preview.innerHTML = safe.replace(regex, m => `<mark>${m}</mark>`);
        }

